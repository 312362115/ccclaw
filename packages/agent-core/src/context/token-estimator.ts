// ============================================================
// Token 估算 — 轻量级字符比例估算
// ============================================================

/**
 * 简单 token 估算：按 1 token ≈ 4 字符计算。
 * 中文场景偏保守（实际 1 token ≈ 1.5-2 中文字符），
 * 但作为上限估算足够安全。
 */
export function estimateTokens(text: string): number {
  return Math.ceil(text.length / 4);
}
