// ============================================================
// 工具使用指南增强 — 按模型能力分级注入
// ============================================================

/**
 * 根据模型能力等级生成工具使用指南。
 * - tier >= 5（强模型如 Opus）：无需额外指导，返回空
 * - tier >= 3（中等模型如 Qwen3.5-Plus）：中等指导
 * - tier < 3（弱模型）：详细的分步指导
 */
export function getToolGuidance(capabilityTier: number): string {
  if (capabilityTier >= 5) {
    return '';
  }

  if (capabilityTier >= 3) {
    return `## 工具使用指南
- 每次只调用一个工具，等待结果后再决定下一步
- 优先使用最直接的工具解决问题，避免不必要的调用
- 工具返回错误时，分析原因后调整参数重试
- 完成任务后直接回复用户，不要额外调用工具`;
  }

  // 弱模型：详细分步指导
  return `## 工具使用指南（请严格遵守）

### 调用规则
1. 每次只调用一个工具，等待返回结果后再继续
2. 调用工具前先想清楚：这个工具能解决当前问题吗？参数填对了吗？
3. 不要编造工具名称，只使用上面列出的工具
4. 不要编造参数值，不确定时先问用户

### 参数格式
- 字符串参数用引号包裹
- 必填参数不能省略
- 参数类型必须匹配（数字不要传字符串）

### 结果处理
- 工具返回成功：基于结果继续任务或回复用户
- 工具返回错误：分析错误原因，调整参数重试（最多 2 次）
- 工具返回空结果：告知用户未找到相关信息

### 完成判断
- 任务完成后直接用文字回复用户
- 不要在回复完成后继续调用工具`;
}
