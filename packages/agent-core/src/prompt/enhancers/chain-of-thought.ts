// ============================================================
// 思维链指南增强 — 按模型能力分级注入
// ============================================================

/**
 * 根据模型能力等级生成思维链指南。
 * - tier >= 5（强模型）：无需额外指导，返回空
 * - tier >= 3（中等模型）：简明的三步思考框架
 * - tier < 3（弱模型）：详细的分步思考模板，要求显式输出思考过程
 */
export function getChainOfThoughtGuidance(capabilityTier: number): string {
  if (capabilityTier >= 5) {
    return '';
  }

  if (capabilityTier >= 3) {
    return `## 思考框架
在每次行动前，请先思考：
1. 我目前已知什么信息？
2. 我还需要什么信息？
3. 下一步最合适的行动是什么？`;
  }

  // 弱模型：详细分步思考模板，要求显式输出思考过程
  return `## 思考框架（请严格遵守）

在每次调用工具或回复用户之前，你必须先输出一段"我的思考："部分，按以下步骤逐项填写：

### 模板
\`\`\`
我的思考：
1. 当前目标：（我现在要完成什么？）
2. 已知信息：（我目前掌握了哪些相关信息？）
3. 缺失信息：（我还缺什么信息才能完成目标？）
4. 行动计划：（我接下来要做什么？为什么选择这个行动？）
5. 风险检查：（这个行动有没有可能出错？出错了怎么办？）
\`\`\`

### 规则
- 每次工具调用前必须输出"我的思考："
- 不允许跳过任何一个步骤
- 如果某一步没有内容，写"无"而不是省略`;
}
