// ============================================================
// 输出格式指南增强 — 按模型能力分级注入
// ============================================================

/**
 * 根据模型能力等级生成输出格式指南。
 * - tier >= 5（强模型）：无需额外指导，返回空
 * - tier >= 3（中等模型）：基础 Markdown 结构指引
 * - tier < 3（弱模型）：显式 Markdown 模板
 */
export function getOutputFormatGuidance(capabilityTier: number): string {
  if (capabilityTier >= 5) {
    return '';
  }

  if (capabilityTier >= 3) {
    return `## 输出格式指南
- 使用 Markdown 格式，层级清晰
- 结构：先给出摘要结论，再展开详细分析，最后总结
- 代码块标注语言类型
- 列表和表格用于结构化信息`;
  }

  // 弱模型：显式 Markdown 模板
  return `## 输出格式指南（请严格遵守）

回复用户时，请按以下 Markdown 模板组织内容：

### 模板
\`\`\`markdown
# 标题

## 摘要
（用 1-3 句话概括核心结论或答案）

## 详细分析
（展开说明，使用小标题、列表、代码块等结构化呈现）

### 子话题 1
- 要点 1
- 要点 2

### 子话题 2
- 要点 1
- 要点 2

## 结论
（总结关键发现和建议的下一步行动）
\`\`\`

### 规则
- 必须包含"摘要"和"结论"两个部分
- 代码块必须标注语言类型（如 \\\`\\\`\\\`typescript）
- 超过 3 个并列项时使用列表或表格
- 不要输出没有内容的空章节`;
}
